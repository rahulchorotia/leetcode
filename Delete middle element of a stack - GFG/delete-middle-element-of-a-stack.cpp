class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sz,int i=0)
    {
        // code here.. 
        if(sz<=0)
            return;
        if(sz/2==i){
            int x =s.top();
            s.pop();
            return;
        }
        else{
            int x =s.top();
            s.pop();
            deleteMid(s,sz,i+1);
            s.push(x);
        }
    }
};

